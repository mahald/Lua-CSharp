print(7 // 2)        -- 3 integer
print(-7 // 2)       -- -4 floor
print(7.5 // 2)      -- 3.0 float floor
print(0xff & 0x0f)   -- 15
print(0x0f | 0xf0)   -- 255
print(0xff ~ 0x0f)   -- 240 (xor)
print(~0)            -- -1
print(~0xff)         -- -256
print(1 << 4)        -- 16
print(256 >> 4)      -- 16
print(math.type(1//1))    -- integer
print(math.type(1.0//1))  -- float
print(math.type(1 & 1))   -- integer
